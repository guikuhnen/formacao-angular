/*
  && ( E ), || ( OU ), ! (Negação)
 */

console.log(1 === 1 && 2 >= 1); // true
console.log(1 === 1 && 1 >= 2); // false

console.log(1 === 1 || 1 >= 2); // true
console.log(1 === 2 || 1 >= 2); // false

console.log(1 !== 2); // true
console.log(2 !== 2); // false

console.log(2 != "2"); // false
console.log(2 !== "2"); // true